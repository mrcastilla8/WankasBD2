  cb.subtotal_estimado,
  
  -- Lógica de descuento 
  (CASE
    WHEN cb.cupon_id IS NULL OR cb.subtotal_estimado = 0 THEN 0
    WHEN cb.subtotal_minimo IS NOT NULL AND cb.subtotal_estimado < cb.subtotal_minimo THEN 0 
    WHEN cb.tipo_descuento = 'monto' 
      THEN LEAST(cb.subtotal_estimado, cb.valor_descuento)
    WHEN cb.tipo_descuento = 'porcentaje' 
      THEN ROUND(cb.subtotal_estimado * (cb.valor_descuento / 100), 2)
    ELSE 0
  END) AS descuento_estimado,

  -- Total Estimado (Subtotal - Descuento)
  (cb.subtotal_estimado - 
    (CASE
      WHEN cb.cupon_id IS NULL OR cb.subtotal_estimado = 0 THEN 0
      WHEN cb.subtotal_minimo IS NOT NULL AND cb.subtotal_estimado < cb.subtotal_minimo THEN 0 
      WHEN cb.tipo_descuento = 'monto' 
        THEN LEAST(cb.subtotal_estimado, cb.valor_descuento)
      WHEN cb.tipo_descuento = 'porcentaje' 
        THEN ROUND(cb.subtotal_estimado * (cb.valor_descuento / 100), 2)
      ELSE 0
    END)
  ) AS total_estimado
