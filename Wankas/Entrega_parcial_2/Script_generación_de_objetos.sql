  CREATE OR REPLACE FORCE EDITIONABLE VIEW "APP_OWNER"."VW_DETALLE_CARRITO" ("CARRITO_ID", "PERFIL_ID", "ESTADO", "CUPON_ID", "CANTIDAD_ITEMS", "SUBTOTAL_ESTIMADO", "DESCUENTO_ESTIMADO", "TOTAL_ESTIMADO") DEFAULT COLLATION "USING_NLS_COMP"  AS 
