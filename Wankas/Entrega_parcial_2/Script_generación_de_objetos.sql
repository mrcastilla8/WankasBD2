carrito_subtotales AS (
  SELECT
    ci.carrito_id,
    COALESCE(SUM(ci.cantidad), 0) AS cantidad_items,
    COALESCE(SUM(ci.cantidad * ci.precio_unitario_observado), 0) AS subtotal_estimado
  FROM
    cart_items ci
  GROUP BY
    ci.carrito_id
),
-- 2. Unir carritos con subtotales y la data del cupón
carrito_base AS (
  SELECT
    c.id AS carrito_id,
    c.perfil_id,
    c.estado,
    c.cupon_id,
    COALESCE(cs.cantidad_items, 0) AS cantidad_items,
    COALESCE(cs.subtotal_estimado, 0) AS subtotal_estimado,
    -- Columnas del cupón que necesita el SELECT final
    cup.tipo_descuento,
    cup.valor_descuento,
    cup.subtotal_minimo
  FROM
    carritos c
  LEFT JOIN
    carrito_subtotales cs ON c.id = cs.carrito_id
  LEFT JOIN
    cupones cup ON c.cupon_id = cup.id
)
-- 3. Calcular descuento estimado y total
SELECT
  cb.carrito_id,
  cb.perfil_id,
  cb.estado,
  cb.cupon_id,
